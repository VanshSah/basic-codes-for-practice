#include<bits/stdc++.h>
using namespace std;
int main()
{
	int a;   // variable declaration
	a = 2;   // initialization of value
	float b;
	char c;
	bool z;
	short x;
	// short ii;
	long long y;
	long t;
	cout<<"size of integer = "<<sizeof(a)<<endl;
	cout<<"size of character = "<<sizeof(c)<<endl;
	cout<<"size of float = "<<sizeof(b)<<endl;
	cout<<"size of boolean = "<<sizeof(z)<<endl;
	cout<<"size of short = "<<sizeof(x)<<endl;
	// cout<<"size of short again = "<<sizeof(ii)<<endl;
	cout<<"size of long long = "<<sizeof(y)<<endl;
	cout<<"size of long = "<<sizeof(t)<<endl;
	cout<<"multiply = "<<a*2<<endl;
//	cout<<(a)pow2<<endl;
}
